def creer(objet,objet_x,objet_y,image):
    objet.x = objet_x
    objet.y = objet_y
    objet.penup()
    objet.hideturtle()
    objet.shape(image)
    objet.setposition(objet.x,objet.y)
    objet.showturtle()
